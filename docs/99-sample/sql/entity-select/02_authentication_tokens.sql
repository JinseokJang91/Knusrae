-- -----------------------------------------------------------------------------
-- Entity: RefreshToken → refresh_tokens
-- -----------------------------------------------------------------------------
SELECT token,
       user_id,
       expires_at,
       created_at
FROM refresh_tokens;

-- -----------------------------------------------------------------------------
-- Entity: TokenBlacklist → token_blacklist
-- -----------------------------------------------------------------------------
SELECT token,
       expires_at,
       blacklisted_at
FROM token_blacklist;
